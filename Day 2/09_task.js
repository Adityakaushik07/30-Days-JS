// Task 9 :
// Write a program to compare two numbers using >= and <= and log the result to the console.

// Using >=
const numOne = 35;
const numTwo = 40;
const result = numOne >= numTwo;

console.log(`Is ${numOne} greater than or equal to ${numTwo}? : ${result}`);

// Using <=

const num1 = 25;
const num2 = 28;
const result2 = num1 <= num2;
console.log(`Is ${num1} less than or equal to ${num2}? : ${result2}`);
