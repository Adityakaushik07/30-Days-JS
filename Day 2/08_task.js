// Task 8 :
// Write a program to compare two numbers using > and < and log the result to the console

// Program for >

const numOne = 45;
const numTwo = 40;
const result = numOne > numTwo;

console.log(`Is ${numOne} is greater than ${numTwo} ? : ${result}`);

// Program for <

const num1 = 60;
const num2 = 55;
const result2 = num1 < num2;

console.log(`Is ${num1} is less than ${num2} ? : ${result2}`);
