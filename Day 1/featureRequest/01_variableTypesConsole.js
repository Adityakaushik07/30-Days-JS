// Feature Request
// 1. Variable Types and console log

// number
const num = 45;

// String
const str = "Hello World";

// boolean
const isLoggedIn = true;

// Array
const arr = [1, 2, 3, 4, 5];

// Object
const info = {
  firstName: "Aditya",
  lastName: "Kaushik",
  age: 21,
};

console.log(`The value of num is ${num} and data type is ${typeof num}`); // Output: number
console.log(`The value of str is ${str} and data type is ${typeof str}`); // Output: String
console.log(
  `The value of isLoggedIn is ${isLoggedIn} and data type is ${typeof isLoggedIn}`
); // Output: boolean
console.log(`The value of arr is ${arr} and data type is ${typeof arr}`); // Output: object
console.log(`The value of info is ${info} and data type is ${typeof info}`); // Output: object
